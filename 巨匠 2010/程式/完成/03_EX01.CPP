#include <stdio.h>
#include <stdlib.h>

int main()
{
   int a=100,intb=2;
   printf("the number a is %d\n",a);
   printf("the number a is %3d\n",intb);

   long int b=80000;
   printf("the number b is %ld\n",b);

   float c=123.456;
   printf("the number c is %f\n",c);

   char d='d';
   printf("the char d is %d\n",d);
   
   system("PAUSE");
   return 0;   
}
